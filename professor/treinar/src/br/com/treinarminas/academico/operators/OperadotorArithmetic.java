package br.com.treinarminas.academico.operators;

public class OperadotorArithmetic {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		int soma = a + b;
		System.out.println("A Soma de " + a + " + " + b + " = " + soma);
		
		int c = 10;
		int d = 20;
		int sub = a - b;
		System.out.println("\nA Subtração de " + c + " - " + d + " = " + sub);
		
		int e = 10;
		int f = 20;
		int mult = a * b;
		System.out.println("\nA Multiplicação de " + e + " - " + f + " = " + mult);
		
		int g = 10;
		int h = 3;
		int div = g / h;
		System.out.println("\nA Divisão de " + g + " / " + h + " = " + div);
		
		int i = 10;
		int j = 3;
		int modulo = g % h;
		System.out.println("\nA Divisão de " + i + " / " + j + " = " + modulo);
		
		
		
		
	}
	
}
