package br.com.treinarminas.academicos.datatypes;

public class Datatypes {
	public static void main (String [] args) {
		byte idadePessoa = 120;
		
		System.out.println("idadePessoa:" + idadePessoa);
		short quantidadeCadeiras = 32000;
		System.out.println("Cadeiras: " + quantidadeCadeiras);
		int i = 10;
		System.out.println("Long: " + i);
		//tipos de dados fracionados
		float f = (float) 15.1;
		float f1 = 151f;
		float f2 = 15.1f;
		System.out.print("Floar  f " + f + " f1: "  + f1 + " f2: " + f2);
		double d = 15.1;
		System.out.println("Double d: " + d);
		
		//tipo de dado logico
		boolean possuiCNH = false;
		System.out.println("Possui CNH " + possuiCNH);
		
		
		char c = 64;
		System.out.println("Char c:" + c);
		System.out.println("linha 1\n\tlinha2");
		
		
		
		

	}

}
