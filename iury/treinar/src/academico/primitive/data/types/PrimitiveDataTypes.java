package academico.primitive.data.types;

public class PrimitiveDataTypes {
	public static void main(String[] args) {
		byte idadePessoa =120;//variavel declara que só aceita ate 128 de numero inteiro. 
			System.out.println("Idade da pesso:"+idadePessoa);
				
		short quantidadeCadeiras = 32000;
			System.out.println("Numero de Cadeiras:"+quantidadeCadeiras);
		long b=100000000;
			System.out.println("long"+b);
		int i=10;
			System.out.println("Inteiro"+i);
		//tipos de dados fracionados. 
		float a=(float)1.79;
			System.out.println("Float"+a);
		float e=1.79F;
			System.out.println("Float"+e);
		float o=1.79f;
			System.out.println("Float"+o);
		double g=18.9;
			System.out.println("Double:"+g);
		//tipo  logico
		boolean possuiCNH = false;
			System.out.println("Possui CNH :"+possuiCNH);
		char c='A';
				System.out.println("Char :" + c);
	
	} 

}
