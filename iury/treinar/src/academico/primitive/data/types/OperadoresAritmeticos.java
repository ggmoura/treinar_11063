package academico.primitive.data.types;

public class OperadoresAritmeticos {
	
	public static void main(String[] args) {
		int a=10;
		int b=20;
		int soma= a + b;
			System.out.println("A soma de: "+a+"+"+b+"="+soma);
		
		int _a=10;
		int _b=20;
		int subtracao= _a - _b;
			System.out.println("A subtração de: "+_a+"-"+_b+"="+subtracao);
	}

}
