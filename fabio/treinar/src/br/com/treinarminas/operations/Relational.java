package br.com.treinarminas.operations;

public class Relational {
	public static void main(String[] args) {
		int a = 10;
		int b = 10;
		System.out.println(a == b);
		boolean valoresIguais = a==b;
		System.out.println(valoresIguais);
		System.out.println(a!=b);
		boolean verdade = true;
		System.out.println(!verdade);

		
		int a1 = 7;
		int b1 = 10;
		System.out.println(a1 > b1);
		boolean valoresMaiores = a1>b1;
		System.out.println(valoresMaiores);
		System.out.println(a1<b1);
		boolean verdade1 = a1<b1;
		System.out.println(!verdade1);
		
		int a2 = 7;
		int b2 = 10;
		System.out.println(a2 >= b2);
		boolean valoresMaioresIgual = a2>=b2;
		System.out.println(valoresMaioresIgual);
		System.out.println(a2<=b2);
		boolean verdade2 = a2<=b2;
		System.out.println(!verdade2);
		
		
		
		
	}

}
